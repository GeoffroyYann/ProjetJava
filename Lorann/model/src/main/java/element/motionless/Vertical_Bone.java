package element.motionless;

public class Vertical_Bone {

    private int x;
    private int y;
    
    public Vertical_Bone(int x , int y){
        this.x = x;
        this.y = y;
    }
    
    public int getX(){
        return x;
    }
    
    public void setX(int x){
        this.x = x;
    }
    
    public int getY(){
        return y;
    }
    
    public void setY(int y){
        this.y = y;
    } 
}
